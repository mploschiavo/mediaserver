logger = logging.getLogger(__name__)


_SUPPORTED_SERVICE_IDS = frozenset(
    {"sonarr", "radarr", "lidarr", "readarr", "prowlarr"}
)
_DEFAULT_HEALTH_PATH = "/ping"
_DEFAULT_PROBE_TIMEOUT_SECONDS = 5
_DEFAULT_API_KEY_FORMAT = "xml"
# Per-arr contracts always set ``api_key_env`` explicitly
# (``SONARR_API_KEY`` / ``RADARR_API_KEY`` / etc.), so this default
# is a safety fallback that should never fire in production. Kept as
# a named constant rather than a literal so the
# ``LifecycleApiKeyHelpers`` wiring stays uniform across adapters.
_DEFAULT_API_KEY_ENV = "SERVARR_API_KEY"

# Stateless module-level singleton — the wirer is per-call
# parameterized by service_id + arr_api_key + ctx, so one instance
# handles every *arr. Constructor-injected notifier identity (name /
# host / port) keeps the magic-number / duplicate-string surface in
# the wirer module rather than here.
_JELLYFIN_NOTIFIER_WIRER = JellyfinNotifierWirer()

# Same shape as the notifier wirer above — stateless module-level
# singleton, per-call parameterized by service_id + arr_api_key +
# ctx. Constructor-injected Prowlarr identity (host / port) keeps
# the magic-number / duplicate-string surface in the wirer module.
_INDEXER_PIPELINE_WIRER = IndexerPipelineWirer()

# Same shape — stateless module-level singleton. The seed-series
# wirer is Sonarr-only (other *arrs short-circuit) and uses the
# Jellyseerr wide-handler delegation pattern: probe is owned, but
# ensure delegates to the legacy handler via injected callables so
# ~100 LoC of multi-subsystem orchestration isn't duplicated here.
_SEED_SERIES_WIRER = SeedSeriesWirer()

# Same shape — stateless module-level singleton, per-call
# parameterized by service_id + arr_api_key + ctx. Per the Bazarr
# monolithic-handler lesson, the runtime-defaults wirer owns N
# distinct probes (quality-profiles + import-lists-auto) but ONE
# shared ensurer that delegates to the legacy
# ``apply_arr_runtime_defaults`` handler — same wide-handler
# delegation pattern the Jellyseerr family uses, because the legacy
# handler does ~100 LoC of multi-arr orchestration in one pass and
# splitting into N ensurers would clobber the shared *arr settings.
_RUNTIME_DEFAULTS_WIRER = RuntimeDefaultsWirer()

# ADR-0005 Phase 5b — the deferred 9th wirer. Same shape as the
# notifier / indexer-pipeline / seed-series instances above:
# stateless module-level singleton, per-call parameterized by
# service_id + arr_api_key + ctx. qBittorrent identity (host /
# port / creds) is constructor-injected so tests can override
# without touching process env. Two promises bind:
# sonarr-download-client (cat=tv) + radarr-download-client
# (cat=movies); lidarr / readarr have no download-client promise
# today but the wirer covers them so the same lifecycle method
# works across the family.
_DOWNLOAD_CLIENT_WIRER = DownloadClientWirer()

# ADR-0005 Phase 5c.1 (wide) — the api-key-discoverable wirer ports
# the legacy ``container_preflight_handlers`` shape into the
# orchestrator's promise model. Stateless module-level singleton:
# per-call parameterized by ``service_id`` + ``ctx``. The wirer's
# ``probe`` matches what ``probe_has_api_key`` already does (env or
# config.xml), with optional ``GET /api/v3/system/status``
# validation. The ``ensure`` extends ``mint_api_key`` by ALSO
# persisting the discovered key to env + the k8s secret — so
# subsequent promises in the same tick (and downstream consumers)
# see the freshly-discovered value without a process restart.
_API_KEY_DISCOVERABLE_WIRER = ServarrApiKeyDiscoverableWirer()


class ServarrLifecycle:
    """``ServiceLifecycle`` implementation for the Servarr family.

    Constructor takes the ``service_id`` ("sonarr", "radarr", ...)
    and stores it as the instance attribute the Protocol requires.
    The same class instance is per-service: bootstrapping all five
    *arr services means five ``ServarrLifecycle`` instances, one each.

    Stateless beyond ``service_id``; per-call config flows through
    ``OrchestrationContext.config``.
    """

    _API_KEY_HELPERS = LifecycleApiKeyHelpers(
        default_api_key_env=_DEFAULT_API_KEY_ENV,
    )

    def __init__(self, service_id: str) -> None:
        sid = (service_id or "").strip().lower()
        if sid not in _SUPPORTED_SERVICE_IDS:
            raise ValueError(
                f"ServarrLifecycle: unsupported service_id={service_id!r}; "
                f"expected one of {sorted(_SUPPORTED_SERVICE_IDS)}. Bazarr "
                "uses BazarrLifecycle (Phase 3); for Jellyfin use "
                "JellyfinLifecycle.",
            )
        self.service_id = sid

    # --- probes -----------------------------------------------------

    def probe_running(self, ctx: OrchestrationContext) -> ProbeResult:
        url = self._ping_url(ctx)
        if not url:
            return ProbeResult.failed(
                "no host/port in config — cannot probe",
                evidence={"config_keys": sorted(ctx.config.keys())},
                evaluated_at=ctx.now(),
            )
        try:
            with urllib.request.urlopen(
                url, timeout=_DEFAULT_PROBE_TIMEOUT_SECONDS,
            ) as resp:
                if resp.status == 200:
                    return ProbeResult.ok(
                        f"responsive at {url}",
                        evidence={"http_status": 200, "url": url},
                        evaluated_at=ctx.now(),
                    )
                return ProbeResult.failed(
                    f"non-200 from {url}: {resp.status}",
                    evidence={"http_status": resp.status, "url": url},
                    evaluated_at=ctx.now(),
                )
        except urllib.error.HTTPError as exc:
            return ProbeResult.failed(
                f"HTTP {exc.code} from {url}",
                evidence={"http_status": exc.code, "url": url},
                evaluated_at=ctx.now(),
            )
        except (urllib.error.URLError, OSError, TimeoutError) as exc:
            return ProbeResult.unknown(
                f"unreachable at {url}: {exc}",
                evidence={"url": url, "error": str(exc)},
                evaluated_at=ctx.now(),
            )

    def probe_has_api_key(self, ctx: OrchestrationContext) -> ProbeResult:
        key = self.discover_api_key(ctx)
        if key:
            return ProbeResult.ok(
                "api key discoverable",
                evidence={
                    "key_length": len(key),
                    "source": self._API_KEY_HELPERS.classify_source(ctx, key),
                },
                evaluated_at=ctx.now(),
            )
        return ProbeResult.failed(
            "no api key in env or config.xml",
            evidence={
                "env_var_checked": self._API_KEY_HELPERS.api_key_env(ctx),
                "config_path": str(self._API_KEY_HELPERS.config_path(ctx)),
            },
            evaluated_at=ctx.now(),
        )

    # --- discover ---------------------------------------------------

    def discover_api_key(self, ctx: OrchestrationContext) -> str | None:
        env_var = self._API_KEY_HELPERS.api_key_env(ctx)
        env_value = (ctx.secrets.get(env_var) or os.environ.get(env_var) or "").strip()
        if env_value:
            return env_value

        config_path = self._API_KEY_HELPERS.config_path(ctx)
        if not config_path or not config_path.is_file():
            return None

        from media_stack.api.services.key_formats import READERS
        fmt = (ctx.config.get("api_key_format") or _DEFAULT_API_KEY_FORMAT).lower()
        reader = READERS.get(fmt)
        if reader is None:
            logger.debug(
                "discover_api_key: no reader for format=%r (service=%s)",
                fmt, self.service_id,
            )
            return None
        try:
            value = reader(config_path)
        except Exception as exc:  # noqa: BLE001
            logger.debug(
                "discover_api_key: %s reader raised: %s", fmt, exc,
            )
            return None
        return value.strip() if value else None

    # --- mint -------------------------------------------------------

    def mint_api_key(self, ctx: OrchestrationContext) -> Outcome[str]:
        # Idempotent short-circuit — the *arr process auto-generates,
        # so if the file already has a key, nothing to do.
        existing = self.discover_api_key(ctx)
        if existing:
            return Outcome.success(
                existing,
                attempts=0,
                evidence={"reason": "already_discoverable"},
            )

        config_path = self._API_KEY_HELPERS.config_path(ctx)
        if not config_path:
            return Outcome.failure(
                "no api_key_config path in config — cannot mint",
                transient=False,
                evidence={"config_keys": sorted(ctx.config.keys())},
            )
        if not config_path.is_file():
            # Service hasn't generated the file yet. This is the
            # warmup case — auto-heal will retry; nothing actionable
            # for the operator unless the service stays unhealthy.
            return Outcome.failure(
                f"{self.service_id} config.xml not yet generated at {config_path}",
                transient=True,
                evidence={
                    "config_path": str(config_path),
                    "service_id": self.service_id,
                },
            )

        # File exists but no key — that's structural, not transient.
        # The *arr process should write the key on first start; if it
        # didn't, manual investigation is needed.
        return Outcome.failure(
            f"{self.service_id} config.xml present but <ApiKey> missing",
            transient=False,
            evidence={
                "config_path": str(config_path),
                "service_id": self.service_id,
            },
        )

    # --- Jellyfin notifier wiring (ADR-0005 Phase 3) ----------------
    #
    # Both methods delegate to ``JellyfinNotifierWirer`` (in
    # ``notifier_wiring.py``). The lifecycle owns the api-key
    # discovery contract; the wirer owns the HTTP / payload shape.

    def probe_jellyfin_notifier(
        self, ctx: OrchestrationContext,
    ) -> ProbeResult:
        return _JELLYFIN_NOTIFIER_WIRER.probe(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    def ensure_jellyfin_notifier(
        self, ctx: OrchestrationContext,
    ) -> Outcome[None]:
        return _JELLYFIN_NOTIFIER_WIRER.ensure(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    # --- Indexer-pipeline wiring (ADR-0005 Phase 3 follow-on) -------
    #
    # Both methods delegate to ``IndexerPipelineWirer`` (in
    # ``indexer_pipeline.py``). The lifecycle owns the api-key
    # discovery contract; the wirer owns the per-arr indexer-list
    # HTTP shape and the Prowlarr ``ApplicationIndexerSync``
    # trigger.

    def probe_has_indexers(
        self, ctx: OrchestrationContext,
    ) -> ProbeResult:
        return _INDEXER_PIPELINE_WIRER.probe(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    def ensure_indexers(
        self, ctx: OrchestrationContext,
    ) -> Outcome[None]:
        return _INDEXER_PIPELINE_WIRER.ensure(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    # --- Seed-series wiring (ADR-0005 Phase 3 — wide-handler) -------
    #
    # The seed-series wirer's probe is owned (count Sonarr's series,
    # idempotent skip when sufficient); the ensurer delegates to the
    # legacy ``ensure_sonarr_seed_series`` handler via injected
    # callables. The legacy handler is wide (~100 LoC: read seed
    # config, fetch qualityprofile + rootfolder + existing series,
    # tvdbId-lookup per seed name, POST each new series). Following
    # the Jellyseerr addendum, we keep that handler as the
    # implementation rather than duplicating it inside the wirer.
    #
    # Lazy imports go through the ``services/`` shim path so the
    # adapters → application hexagon ratchet stays clean — the
    # canonical ``application.jobs.framework`` module is reached
    # transitively via the shim, not by direct import here. Same
    # convention the Jellyseerr arr-servers ensurer follows.

    def probe_has_series(
        self, ctx: OrchestrationContext,
    ) -> ProbeResult:
        return _SEED_SERIES_WIRER.probe(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    def ensure_has_series(
        self, ctx: OrchestrationContext,
    ) -> Outcome[None]:
        from media_stack.services.apps.core.job_adapters import (
            ensure_sonarr_seed_series,
        )
        from media_stack.services.jobs.framework import JobContext
        return _SEED_SERIES_WIRER.ensure(
            self.service_id, self.discover_api_key(ctx), ctx,
            configure_handler=ensure_sonarr_seed_series,
            job_context_factory=JobContext,
        )

    # --- Runtime-defaults wiring (ADR-0005 Phase 3) -----------------
    #
    # Three promises bind here:
    #   * sonarr-quality-profiles    → probe_quality_profiles
    #   * radarr-quality-profiles    → probe_quality_profiles
    #   * radarr-import-lists-auto   → probe_import_lists_auto
    # All three share ``ensure_runtime_defaults`` (rationale on the
    # wirer module top: legacy handler is monolithic — splitting into
    # per-promise ensurers would mean three POST runs that each
    # clobber the shared *arr settings document).
    #
    # ``probe_import_lists_auto`` short-circuits ``ok`` for non-radarr
    # service ids using the unsupported-service pattern from
    # ``JellyfinNotifierWirer``; sonarr lifecycle instances will never
    # have a promise that calls it, but the wirer guards anyway.

    def probe_quality_profiles(
        self, ctx: OrchestrationContext,
    ) -> ProbeResult:
        return _RUNTIME_DEFAULTS_WIRER.probe_quality_profiles(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    def probe_import_lists_auto(
        self, ctx: OrchestrationContext,
    ) -> ProbeResult:
        return _RUNTIME_DEFAULTS_WIRER.probe_import_lists_auto(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    def ensure_runtime_defaults(
        self, ctx: OrchestrationContext,
    ) -> Outcome[None]:
        return _RUNTIME_DEFAULTS_WIRER.ensure_runtime_defaults(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    # --- Download-client wiring (ADR-0005 Phase 5b) -----------------
    #
    # Two promises bind:
    #   * sonarr-download-client → probe_download_client / ensure
    #   * radarr-download-client → probe_download_client / ensure
    #
    # Both methods delegate to ``DownloadClientWirer`` (in
    # ``download_client_wiring.py``). The lifecycle owns the api-key
    # discovery contract; the wirer owns the per-arr endpoint shape,
    # the qBittorrent payload, and the upsert-by-implementation
    # match logic.
    #
    # Lidarr / Readarr have no download-client promise today, but
    # the wirer's ``_ARR_DOWNLOAD_CLIENT_SPECS`` table covers them
    # too — so adding a future ``lidarr-download-client`` promise is
    # a one-line YAML change rather than a wirer rewrite.

    def probe_download_client(
        self, ctx: OrchestrationContext,
    ) -> ProbeResult:
        return _DOWNLOAD_CLIENT_WIRER.probe(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    def ensure_download_client(
        self, ctx: OrchestrationContext,
    ) -> Outcome[None]:
        return _DOWNLOAD_CLIENT_WIRER.ensure(
            self.service_id, self.discover_api_key(ctx), ctx,
        )

    # --- API-key-discoverable wiring (ADR-0005 Phase 5c.1 wide) -----
    #
    # Four promises bind:
    #   * sonarr-api-key-discoverable
    #   * radarr-api-key-discoverable
    #   * lidarr-api-key-discoverable
    #   * readarr-api-key-discoverable
    # All four share ``ServarrApiKeyDiscoverableWirer`` — same probe
    # (env-or-disk + optional HTTP validation) + same ensure
    # (read-from-disk + persist-to-env + k8s-secret patch). Replaces
    # the legacy ``container_preflight_handlers`` ``run_preflight``
    # invocation under ``_run_preflights``.

    def probe_api_key_discoverable(
        self, ctx: OrchestrationContext,
    ) -> ProbeResult:
        return _API_KEY_DISCOVERABLE_WIRER.probe(self.service_id, ctx)

    def ensure_api_key_discoverable(
        self, ctx: OrchestrationContext,
    ) -> Outcome[None]:
        return _API_KEY_DISCOVERABLE_WIRER.ensure(self.service_id, ctx)

    # --- persist ----------------------------------------------------

    def persist_api_key(
        self, key: str, ctx: OrchestrationContext,
    ) -> Outcome[None]:
        env_var = self._API_KEY_HELPERS.api_key_env(ctx)
        if not key:
            return Outcome.failure(
                "refusing to persist empty key",
                transient=False,
                evidence={"env_var": env_var},
            )
        os.environ[env_var] = key

        try:
            from media_stack.services.apps.core.job_adapters import (
                _persist_preflight_keys_to_secret_safe,
                _stub_state,
            )
            secret_result = _persist_preflight_keys_to_secret_safe(
                _stub_state(), {env_var: key},
            )
            return Outcome.success(
                evidence={
                    "env_written": env_var,
                    "secret_status": str(
                        (secret_result or {}).get("status") or secret_result,
                    ),
                },
            )
        except Exception as exc:  # noqa: BLE001
            return Outcome.failure(
                f"env written; secret patch failed: {exc}",
                transient=True,
                evidence={"env_written": env_var, "error": str(exc)},
            )

    # --- helpers ----------------------------------------------------

    def _ping_url(self, ctx: OrchestrationContext) -> str:
        host = (ctx.config.get("host") or "").strip()
        port = ctx.config.get("port")
        if not host or not port:
            return ""
        scheme = (ctx.config.get("scheme") or "http").strip()
        path = ctx.config.get("health_path") or _DEFAULT_HEALTH_PATH
        return f"{scheme}://{host}:{port}{path}"


