#!/usr/bin/env python3
"""Generate docs/reference/promises.md from contracts/promises.yaml.

The promises registry is the source of truth for "what works OTB after
a fresh install." This script renders a human-readable reference table
so the docs can't drift from the registry — re-run after editing
``contracts/promises.yaml``.
"""

from __future__ import annotations

import sys
from pathlib import Path

import yaml

REPO_ROOT = Path(__file__).resolve().parent.parent
PROMISES_YAML = REPO_ROOT / "contracts" / "promises.yaml"
OUTPUT = REPO_ROOT / "docs" / "reference" / "promises.md"


def render() -> str:
    data = yaml.safe_load(PROMISES_YAML.read_text(encoding="utf-8"))
    promises = data.get("promises") or []

    lines: list[str] = []
    lines.append("<!-- AUTO-GENERATED by bin/render-promises-reference.py -->")
    lines.append("<!-- Source: contracts/promises.yaml. Edit the YAML, then re-run the script. -->")
    lines.append("")
    lines.append("# Promises Reference")
    lines.append("")
    lines.append(
        "Every claim like \"after a fresh install, Bazarr has English subtitles enabled\" "
        "lives in [`contracts/promises.yaml`](../../contracts/promises.yaml) as a single "
        "declarative entry. Three things consume that file:"
    )
    lines.append("")
    lines.append("1. The meta-ratchet (`tests/unit/test_promises_registry.py`) — verifies every promise points at a real handler.")
    lines.append("2. The fresh-install verifier (`bin/verify-fresh-install.sh`) — runs each promise's probe against a live stack.")
    lines.append("3. This page — the human-readable index.")
    lines.append("")
    lines.append("## How to read the table")
    lines.append("")
    lines.append(
        "- **Promise** — the claim. After a clean bootstrap, the promise should hold and stay holding.\n"
        "- **Ensured by** — the contract job (or `post_setup_handler`) whose handler makes the promise true.\n"
        "- **Probe** — what the verifier checks to prove the promise actually holds in a live stack."
    )
    lines.append("")
    lines.append(f"There are **{len(promises)} promises** at this commit.")
    lines.append("")
    lines.append("## The promises")
    lines.append("")
    lines.append("| Promise | Ensured by | Probe |")
    lines.append("|---|---|---|")

    for p in promises:
        pid = str(p.get("id") or "").strip()
        desc = str(p.get("description") or "").strip().replace("|", "\\|")
        ensured = str(p.get("ensured_by") or "").strip()
        probe = p.get("probe") or {}
        ptype = str(probe.get("type") or "").strip()
        if ptype.startswith("http"):
            target = f"`{ptype}` &rarr; {probe.get('service', '')}{probe.get('path', '')}"
        elif ptype.startswith("file"):
            target = f"`{ptype}` &rarr; {probe.get('path', '')}"
        else:
            target = f"`{ptype}`"
        lines.append(f"| **{pid}** &mdash; {desc} | `{ensured}` | {target} |")

    lines.append("")
    lines.append("## Adding a new promise")
    lines.append("")
    lines.append("1. Write the `ensure_*` adapter that re-asserts the desired state. Idempotent &mdash; every run leaves the system in the same end state.")
    lines.append("2. Register a contract job pointing at the adapter (`phase: post`).")
    lines.append("3. Add an entry to [`contracts/promises.yaml`](../../contracts/promises.yaml) with a probe that proves it works.")
    lines.append("4. Re-run this script: `python3 bin/render-promises-reference.py`.")
    lines.append("")
    lines.append("If steps 1+2 happen but step 3 doesn't, the meta-ratchet warns (orphan adapter); if step 3 happens without 1+2, the meta-ratchet fails hard (broken promise).")
    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("**Project Steward**")
    lines.append("Matthew Loschiavo &middot; [matthewloschiavo.com](https://matthewloschiavo.com) &middot; [mploschiavo@gmail.com](mailto:mploschiavo@gmail.com) &middot; [LinkedIn](https://www.linkedin.com/in/matthewloschiavo)")
    lines.append("")

    return "\n".join(lines)


def main() -> int:
    if not PROMISES_YAML.is_file():
        print(f"ERROR: missing {PROMISES_YAML}", file=sys.stderr)
        return 1
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT.write_text(render(), encoding="utf-8")
    print(f"wrote {OUTPUT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
