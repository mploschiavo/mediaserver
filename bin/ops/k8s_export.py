#!/usr/bin/env python3
"""Export deployment-specific Kubernetes config so you can re-apply
it on a clean cluster after a teardown.

Captures the resources that carry *operator-owned* state — the bits a
fresh ``./deploy-k8s.sh`` won't know about because they were edited
in-cluster, supplied via cert-manager, or generated by the controller:

  * **TLS Secrets** — every ``kubernetes.io/tls`` Secret in the
    namespace (cert + private key bundles). The default profile
    expects cert-manager to mint these, but operators routinely
    paste in a known cert chain or import a private CA. Without
    this export, those certs are gone after a namespace delete.

  * **Ingress / Gateway / HTTPRoute / VirtualService objects** —
    routing rules edited in-cluster (e.g. annotations for upstream
    timeouts, rewrites, custom hostnames) that diverged from the
    Kustomize base.

  * **ConfigMaps with operator-owned data** — anything labelled
    ``media-stack.local/operator-owned=true`` (covers route maps,
    Authelia overrides, custom envoy snippets). Filterable.

  * **Generic Secrets** (opt-in via ``--include-secrets`` because
    they may contain plaintext API keys; the default behaviour is
    "TLS only" so an absent-minded ``--scope=all`` doesn't dump
    creds into a public gist).

The export is cluster-clean: ``managedFields``, ``resourceVersion``,
``uid``, ``creationTimestamp``, and the auto-generated
``last-applied-configuration`` annotation are stripped so the YAML
applies cleanly to a fresh cluster without conflicts.

Output:

  * Each resource as a separate YAML document under a per-kind
    folder (``tls/``, ``ingress/``, ``configmaps/``, ``secrets/``).
  * A ``manifest.yaml`` at the root concatenating everything in
    the order kubectl expects (Namespace → Secrets → ConfigMaps →
    Routing).
  * A ``RESTORE.md`` with the exact ``kubectl apply -f`` sequence.

Restore: ``kubectl apply -f <export-dir>/manifest.yaml`` after
``./deploy-k8s.sh`` brings the namespace back. The TLS secrets must
land BEFORE Ingress / Gateway resources reference them — the
manifest order handles that.
"""

from __future__ import annotations

import argparse
import json
import os
import shutil
import subprocess
import sys
from pathlib import Path
from typing import Any, Iterable

DEFAULT_NAMESPACE = "media-stack"

# Annotation strip-list. ``last-applied-configuration`` carries the
# pre-server-side-apply state and re-applying it confuses kubectl;
# the others are server-injected and not portable across clusters.
STRIP_ANNOTATIONS = (
    "kubectl.kubernetes.io/last-applied-configuration",
    "deployment.kubernetes.io/revision",
)
STRIP_METADATA_FIELDS = (
    "creationTimestamp",
    "resourceVersion",
    "uid",
    "generation",
    "selfLink",
    "managedFields",
    "ownerReferences",
)
# Field path inside ``status`` is dropped wholesale — every field in
# .status is server-managed by the controller manager.
STRIP_TOP_LEVEL_FIELDS = ("status",)

# Apply-order priority (lower = applied first). A fresh cluster
# needs Secrets present before Ingress / Gateway resources can
# reference them; Namespace must be first.
KIND_PRIORITY: dict[str, int] = {
    "Namespace": 0,
    "Secret": 10,
    "ConfigMap": 20,
    "Service": 30,
    "Ingress": 40,
    "Gateway": 41,
    "HTTPRoute": 42,
    "VirtualService": 43,
}


def has_kubectl() -> bool:
    return shutil.which("kubectl") is not None


def has_yaml_module() -> bool:
    try:
        import yaml  # noqa: F401
    except ImportError:
        return False
    return True


def kubectl_get_json(
    kind: str, namespace: str, label_selector: str | None = None,
) -> dict[str, Any]:
    """Run ``kubectl get <kind> -n <ns> -o json``. Returns the
    parsed dict. Empty list shape on failure (we don't want one
    missing API resource to abort the whole export)."""
    cmd = ["kubectl", "get", kind, "-n", namespace, "-o", "json"]
    if label_selector:
        cmd.extend(["-l", label_selector])
    try:
        result = subprocess.run(
            cmd, capture_output=True, text=True, timeout=30, check=True,
        )
    except (
        subprocess.CalledProcessError,
        subprocess.TimeoutExpired,
        FileNotFoundError,
    ) as exc:
        sys.stderr.write(
            f"[WARN] kubectl get {kind} failed: {exc}\n",
        )
        return {"items": []}
    try:
        return json.loads(result.stdout)
    except json.JSONDecodeError:
        return {"items": []}


def sanitize_resource(obj: dict[str, Any]) -> dict[str, Any]:
    """Strip cluster-injected fields so the YAML re-applies cleanly."""
    out = dict(obj)
    for field in STRIP_TOP_LEVEL_FIELDS:
        out.pop(field, None)
    metadata = dict(out.get("metadata") or {})
    annotations = dict(metadata.get("annotations") or {})
    for ann in STRIP_ANNOTATIONS:
        annotations.pop(ann, None)
    if annotations:
        metadata["annotations"] = annotations
    else:
        metadata.pop("annotations", None)
    for field in STRIP_METADATA_FIELDS:
        metadata.pop(field, None)
    out["metadata"] = metadata
    return out


def apply_priority(resource: dict[str, Any]) -> int:
    return KIND_PRIORITY.get(resource.get("kind", ""), 99)


def collect_resources(
    namespace: str,
    *,
    include_secrets: bool,
    operator_owned_label: str,
) -> list[dict[str, Any]]:
    """Pull every resource we want to capture into a single list."""
    out: list[dict[str, Any]] = []

    # Namespace itself — recreated by ``./deploy-k8s.sh`` but having
    # the explicit object preserves any custom labels the operator
    # added (e.g. for network policies).
    ns_blob = kubectl_get_json("namespace", namespace)
    if isinstance(ns_blob, dict) and ns_blob.get("kind") == "Namespace":
        out.append(sanitize_resource(ns_blob))

    # TLS secrets — always included. Strip the secret data only if
    # the operator passes --redact (default keeps the cert/key — the
    # whole point of the export is to roundtrip these).
    secrets = kubectl_get_json("secrets", namespace).get("items", [])
    for s in secrets:
        secret_type = s.get("type", "")
        if secret_type == "kubernetes.io/tls":
            out.append(sanitize_resource(s))
        elif include_secrets:
            out.append(sanitize_resource(s))

    # ConfigMaps labelled as operator-owned.
    cms = kubectl_get_json(
        "configmaps", namespace, label_selector=operator_owned_label,
    ).get("items", [])
    for cm in cms:
        out.append(sanitize_resource(cm))

    # Routing — Ingress (k8s native) + Gateway API (HTTPRoute) +
    # Istio (VirtualService). Each gets its own kubectl call;
    # absent CRDs fail soft via ``kubectl get`` returning empty.
    for kind in ("ingresses", "gateways", "httproutes", "virtualservices"):
        items = kubectl_get_json(kind, namespace).get("items", [])
        for item in items:
            out.append(sanitize_resource(item))

    return sorted(out, key=lambda r: (apply_priority(r),
                                      r.get("kind", ""),
                                      _name(r)))


def _name(resource: dict[str, Any]) -> str:
    return ((resource.get("metadata") or {}).get("name") or "")


def write_export(
    out_dir: Path,
    resources: list[dict[str, Any]],
    namespace: str,
) -> None:
    """Write each resource to ``<kind>/<name>.yaml`` and the
    concatenated ``manifest.yaml`` at the root."""
    import yaml  # local import — checked elsewhere

    out_dir.mkdir(parents=True, exist_ok=True)
    manifest_path = out_dir / "manifest.yaml"

    written_per_kind: dict[str, int] = {}
    with manifest_path.open("w", encoding="utf-8") as manifest:
        for i, r in enumerate(resources):
            kind = (r.get("kind") or "Unknown").lower()
            name = _name(r) or f"unnamed-{i}"
            kind_dir = out_dir / kind
            kind_dir.mkdir(exist_ok=True)
            target = kind_dir / f"{name}.yaml"
            yaml_doc = yaml.safe_dump(r, sort_keys=False)
            target.write_text(yaml_doc, encoding="utf-8")
            if i > 0:
                manifest.write("---\n")
            manifest.write(yaml_doc)
            written_per_kind[r.get("kind", "Unknown")] = (
                written_per_kind.get(r.get("kind", "Unknown"), 0) + 1
            )

    restore = out_dir / "RESTORE.md"
    restore.write_text(
        _render_restore_md(namespace, written_per_kind),
        encoding="utf-8",
    )


def _render_restore_md(
    namespace: str, counts: dict[str, int],
) -> str:
    lines = [
        "# Restore Instructions\n",
        "",
        f"This export captures the operator-owned state of the "
        f"`{namespace}` namespace at the time of the dump.\n",
        "",
        "## What's included\n",
        "",
    ]
    for kind, n in sorted(counts.items()):
        lines.append(f"- **{kind}**: {n}")
    lines += [
        "",
        "## How to apply\n",
        "",
        "1. Bring the cluster back up:",
        "",
        "       ./deploy-k8s.sh",
        "",
        "2. Wait for the namespace to settle (cert-manager / Authelia "
        "/ controller pods Ready):",
        "",
        f"       kubectl -n {namespace} rollout status "
        "deploy/media-stack-controller",
        "",
        "3. Apply this export — Secrets land before Routing so "
        "Ingress objects can resolve their `tls.secretName` "
        "references:",
        "",
        "       kubectl apply -f manifest.yaml",
        "",
        "## What's NOT in here\n",
        "",
        "- Application data inside the controller's PVC "
        "(`/srv-config`).",
        "- Container images / Deployments — those come from `./deploy-k8s.sh`.",
        "- Generic (non-TLS) Secrets, unless you ran with "
        "`--include-secrets`.",
    ]
    return "\n".join(lines) + "\n"


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description=(
            "Export operator-owned k8s state (TLS, routing, "
            "operator ConfigMaps) so it can be restored after a "
            "namespace teardown."
        ),
    )
    p.add_argument(
        "--namespace", "-n",
        default=DEFAULT_NAMESPACE,
        help=f"Namespace to export (default: {DEFAULT_NAMESPACE})",
    )
    p.add_argument(
        "--out", "-o",
        default="./k8s-export",
        help="Output directory (default: ./k8s-export)",
    )
    p.add_argument(
        "--include-secrets", action="store_true",
        help=(
            "Include non-TLS Secrets in the export. Off by default "
            "to avoid dumping plaintext API keys."
        ),
    )
    p.add_argument(
        "--operator-owned-label",
        default="media-stack.local/operator-owned=true",
        help=(
            "Label selector for ConfigMaps to capture. Operators "
            "label CMs they want round-tripped; the kustomize base "
            "doesn't set this label, so unmodified CMs are skipped."
        ),
    )
    p.add_argument(
        "--dry-run", action="store_true",
        help=(
            "Print the resource list and exit without writing files."
        ),
    )
    return p.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    if not has_kubectl():
        sys.stderr.write(
            "[ERR] kubectl is not on PATH — install it first.\n",
        )
        return 1
    if not has_yaml_module():
        sys.stderr.write(
            "[ERR] PyYAML is required (pip install pyyaml).\n",
        )
        return 1

    print(f"==> Exporting namespace '{args.namespace}'")
    resources = collect_resources(
        args.namespace,
        include_secrets=args.include_secrets,
        operator_owned_label=args.operator_owned_label,
    )
    print(f"    {len(resources)} resources captured")
    by_kind: dict[str, int] = {}
    for r in resources:
        kind = r.get("kind", "Unknown")
        by_kind[kind] = by_kind.get(kind, 0) + 1
    for kind, n in sorted(by_kind.items()):
        print(f"      - {kind}: {n}")

    if args.dry_run:
        print("[OK] dry-run; no files written.")
        return 0

    out_dir = Path(args.out).resolve()
    write_export(out_dir, resources, args.namespace)
    print(f"==> Wrote export to {out_dir}")
    print(f"    Restore with: kubectl apply -f {out_dir / 'manifest.yaml'}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
